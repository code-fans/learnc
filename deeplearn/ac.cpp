/*
 * ================================================================
 *        小小神经网络 —— 教电脑分辨英文和拼音
 * ================================================================
 *
 *  想象你是一个侦探，要判断一段文字是英文还是拼音。
 *  你会怎么判断呢？你可能会看：
 *    - 有没有字母 q、x？（拼音里特别多：qi、xi、qing...）
 *    - 有没有 zh、ch、sh？（zhi、chi、shi 是拼音特有的）
 *    - 词尾是不是 s、d、k？（英文词经常这样结尾）
 *
 *  神经网络做的事情和侦探一样，只不过它不会直接看文字，
 *  它只懂数字。所以我们的计划是：
 *
 *  ┌──────────┐    ┌──────────┐    ┌──────────┐
 *  │ 第1步     │ →  │ 第2步     │ →  │ 第3步     │
 *  │ 把文字变成 │    │ 送进神经  │    │ 投票决定  │
 *  │ 数字(特征) │    │ 网络判断   │    │ 最终答案  │
 *  └──────────┘    └──────────┘    └──────────┘
 *
 *  第1步 - 特征提取（把一个单词变成 7 个数字）：
 *    数字1: 单词有几个字母 ÷ 8（拼音词通常比较短）
 *    数字2: 里面有没有字母 q？（有=1，没有=0）
 *    数字3: 里面有没有字母 x？
 *    数字4: 里面有没有 zh、ch、sh？
 *    数字5: 最后一个字母是不是拼音不用的字母？（比如 s、d、k）
 *    数字6: 有没有两个辅音挨在一起？（比如 th、br、st）
 *    数字7: 元音字母占了几成？
 *
 *  第2步 - 神经网络判断：
 *    网络像一个"数字处理器"，把 7 个数字吃进去，
 *    吐出一个 0~1 之间的分数。接近 1 = 英文，接近 0 = 拼音。
 *
 *  第3步 - 投票：
 *    一句话有很多单词，我们对每个单词都判断一次，
 *    然后数数：认为是英文的多，还是拼音的多？多数获胜！
 *
 *  网络长这样：
 *    7个数字 ──→ [隐藏层:4个小脑] ──→ 1个分数
 *    (输入层)     (中间层)           (输出层)
 *
 * ================================================================
 */

#include <iostream>
#include <string>
#include <vector>
#include <cmath>
#include <cstdlib>
using namespace std;

// ============================================================
//  激活函数 sigmoid —— 网络内部的"软开关"
//
//  想象一个水龙头：
//  - 往左拧（输入很小）→ 水流接近 0
//  - 往右拧（输入很大）→ 水流接近 1
//  - 中间位置          → 水流在 0 和 1 之间慢慢变化
//
//  为什么不用"硬开关"（要么0要么1）？
//  因为硬开关没有"中间状态"，没法微调，网络就学不了东西。
//  sigmoid 平滑过渡，让网络可以一点一点地学习。
// ============================================================
double sigmoid(double x) { return 1.0 / (1.0 + exp(-x)); }

// sigmoid 的"变化率"——告诉网络"现在往哪个方向调整最有效"
// 好比你在猜数字：猜大了就往小调，猜小了就往大调。
// 这个函数告诉你"应该调多少"。
double sig_d(double s) { return s * (1.0 - s); }

// ============================================================
//  神经网络类 —— 我们的小小"大脑"
//
//  你可以把它想象成 3 排灯泡：
//
//  第1排(输入层):  7 个灯泡，分别对应一个单词的 7 个数字
//  第2排(隐藏层):  4 个灯泡，负责综合分析
//  第3排(输出层):  1 个灯泡，亮=英文，暗=拼音
//
//  灯泡之间有"连线"，每根连线有一个"权重"（粗细）：
//  - 连线粗 = 这个信号很重要
//  - 连线细 = 这个信号不太重要
//
//  "学习"就是不断调整这些连线的粗细，让答案越来越准。
// ============================================================
class NeuralNet {
public:
    int ni, nh;

    // W 和 b 就是那些"连线的粗细"——这是网络唯一需要学习的东西
    vector<vector<double>> W1; // 第1排→第2排，每根连线的权重
    vector<double> b1;         // 第2排每个灯泡的"基础亮度"
    vector<double> W2;         // 第2排→第3排，每根连线的权重
    double b2;                 // 第3排灯泡的"基础亮度"

    // 刚创建时，连线粗细是随机的（就像刚出生的宝宝，啥也不会）
    NeuralNet(int in, int hid)
        : ni(in), nh(hid),
          W1(hid, vector<double>(in)), b1(hid, 0),
          W2(hid, 0), b2(0) {
        srand(123);
        for (auto& r : W1) for (auto& w : r) w = (rand()%1000-500)/500.0;
        for (auto& w : W2)           w = (rand()%1000-500)/500.0;
    }

    // ---- 前向传播 ----
    // 就像信号从左到右传递：
    // 第1排灯泡亮起来 → 信号通过连线传到第2排 → 第2排亮起来
    // → 信号继续传到第3排 → 第3排亮起来 → 这就是预测结果！
    double forward(const vector<double>& in, vector<double>& hid) {
        // 第2排（隐藏层）：把第1排的信号加权求和，再过sigmoid
        hid.resize(nh);
        for (int j = 0; j < nh; j++) {
            double s = b1[j];
            for (int i = 0; i < ni; i++)
                s += W1[j][i] * in[i];   // 信号 × 连线粗细，然后加起来
            hid[j] = sigmoid(s);          // 过"软开关"，得到0~1的亮度
        }
        // 第3排（输出层）：把第2排的信号再加权求和
        double s = b2;
        for (int j = 0; j < nh; j++)
            s += W2[j] * hid[j];
        return sigmoid(s);               // 最终预测：接近1=英文，接近0=拼音
    }

    // ---- 反向传播（训练）----
    // 这是网络"学习"的秘密武器！
    //
    // 打个比方：你考试得了80分，目标是100分。
    // 你会想"差了20分，我哪里没做好？"然后去补那些地方。
    //
    // 网络也一样：
    // 1) 先猜一个答案（前向传播）
    // 2) 看看和正确答案差多少（误差）
    // 3) 把误差"倒推回去"，算出每根连线应该调粗还是调细
    // 4) 微调所有连线
    //
    // 重复很多次，网络就学会了！
    void train(const vector<double>& in, double target, double lr) {
        vector<double> hid;
        double out = forward(in, hid);              // 第1步：先猜
        double oe = (target - out) * sig_d(out);    // 第2步：算误差

        // 第3、4步：倒推回去，调整第1排→第2排的连线
        for (int j = 0; j < nh; j++) {
            double he = oe * W2[j] * sig_d(hid[j]);
            for (int i = 0; i < ni; i++)
                W1[j][i] += lr * he * in[i];        // 微调连线粗细
            b1[j] += lr * he;
        }
        // 调整第2排→第3排的连线
        for (int j = 0; j < nh; j++)
            W2[j] += lr * oe * hid[j];
        b2 += lr * oe;
    }

    // 预测（只做前向传播，不调整连线——考试时不学习！）
    double predict(const vector<double>& in) {
        vector<double> hid;
        return forward(in, hid);
    }
};

// ============================================================
//  特征提取 —— 把一个单词"翻译"成 7 个数字
//
//  神经网络只认识数字，不认识字母。
//  所以我们要当一个"翻译官"，把单词的特点变成数字。
//
//  举个例子：单词 "quick"
//    数字1 = 5/8 = 0.625  （5个字母）
//    数字2 = 1             （有字母 q）
//    数字3 = 0             （没有字母 x）
//    数字4 = 0             （没有 zh/ch/sh）
//    数字5 = 1             （以 k 结尾，拼音不会这样）
//    数字6 = 1             （ck 是连续辅音）
//    数字7 = 2/5 = 0.4    （5个字母里有2个元音）
//
//  再比如：单词 "qi"
//    数字1 = 2/8 = 0.25   （2个字母，很短）
//    数字2 = 1             （有字母 q）
//    数字3 = 0             （没有字母 x）
//    数字4 = 0             （没有 zh/ch/sh）
//    数字5 = 0             （以 i 结尾，拼音可以）
//    数字6 = 0             （没有连续辅音）
//    数字7 = 1/2 = 0.5    （2个字母里有1个元音）
//
//  看到了吗？"quick" 和 "qi" 的数字很不一样！
//  这就是网络能区分它们的秘密。
// ============================================================
vector<double> word_feat(const string& w) {
    int has_q = 0, has_x = 0, has_zhcs = 0, vowels = 0, cc = 0;

    for (int i = 0; i < (int)w.size(); i++) {
        // 检查字母 q（拼音里超多：qi、qing、quan...）
        if (w[i] == 'q') has_q = 1;
        // 检查字母 x（拼音里超多：xi、xing、xue...）
        if (w[i] == 'x') has_x = 1;
        // 数元音（a e i o u）有几个
        if (w[i] == 'a' || w[i] == 'e' || w[i] == 'i' || w[i] == 'o' || w[i] == 'u')
            vowels++;

        // 检查相邻的两个字母
        if (i + 1 < (int)w.size()) {
            // 两个都是辅音（不是 a e i o u）吗？
            bool c1 = !(w[i]=='a'||w[i]=='e'||w[i]=='i'||w[i]=='o'||w[i]=='u');
            bool c2 = !(w[i+1]=='a'||w[i+1]=='e'||w[i+1]=='i'||w[i+1]=='o'||w[i+1]=='u');
            if (c1 && c2) {
                // zh、ch、sh 是拼音特有的
                if ((w[i]=='z'&&w[i+1]=='h')||(w[i]=='c'&&w[i+1]=='h')||(w[i]=='s'&&w[i+1]=='h'))
                    has_zhcs = 1;
                else
                    cc = 1;  // 其他连续辅音（th、br、st...）是英文特有的
            }
        }
    }

    // 检查词尾：拼音的词只以 a e i o u n g r 结尾
    // 如果以其他字母结尾（比如 s、d、k、t、y），那一定是英文
    int end_eng = 0;
    char c = w.back();
    if (c!='a'&&c!='e'&&c!='i'&&c!='o'&&c!='u'&&c!='n'&&c!='g'&&c!='r')
        end_eng = 1;

    // 把所有观察结果打包成 7 个数字
    return {
        (double)w.size() / 8.0,       // 数字1: 单词长度
        (double)has_q,                  // 数字2: 有没有 q
        (double)has_x,                  // 数字3: 有没有 x
        (double)has_zhcs,               // 数字4: 有没有 zh/ch/sh
        (double)end_eng,                // 数字5: 词尾是不是拼音不用的字母
        (double)cc,                     // 数字6: 有没有连续辅音
        (double)vowels / w.size()       // 数字7: 元音占比
    };
}

// ============================================================
//  主函数 —— 程序从这里开始运行
// ============================================================
int main() {
    // 创建神经网络：7个输入，4个隐藏神经元
    NeuralNet nn(7, 4);

    // ---- 准备训练数据 ----
    // 就像教小朋友认字，我们要给网络看一些例子：
    // "这些是英文单词，那些是拼音单词"
    vector<pair<vector<double>, double>> data;

    // 15 个英文单词（告诉网络：这些 → 输出应该接近 1）
    const char* en[] = {
        // 有连续辅音(th,br等)
        "the","this","brown","quick","jumps",
        // 英文词尾(s,d等)
        "is","has","was","read","find",
        // 其他常见英文词
        "have","will","of","in","to",
    };

    // 15 个拼音单词（告诉网络：这些 → 输出应该接近 0）
    const char* cn[] = {
        // 含 q/x
        "qi","xiang","xue","xiao","qing",
        // 含 zh/ch/sh
        "zhi","shi","zhan","chang","cheng",
        // 短词
        "zhe","ge","de","he","men",
    };

    // 先把所有单词"翻译"成数字（提取特征）
    for (auto w : en) data.push_back({word_feat(w), 1.0});  // 1.0 = 英文
    for (auto w : cn) data.push_back({word_feat(w), 0.0});  // 0.0 = 拼音

    // ---- 训练！----
    // 把这些例子反复给网络看 200 遍，让它慢慢学会区分
    // 就像你背乘法口诀，多背几遍就记住了
    for (int epoch = 0; epoch < 200; epoch++)
        for (auto& p : data)
            nn.train(p.first, p.second, 5.0);

    // ---- 考试时间！----
    // 读入测试数据，对每个单词判断一次，然后投票
    ios::sync_with_stdio(false);
    int T; cin >> T;
    while (T--) {
        int n; cin >> n;
        int eng = 0;  // 记票：有多少个单词被认成英文
        for (int i = 0; i < n; i++) {
            string w; cin >> w;
            if (nn.predict(word_feat(w)) > 0.5)
                eng++;  // 网络说这个单词是英文，英文+1票
        }
        // 投票结果：认为英文的多就输出 English，否则输出 Pinyin
        cout << (eng > n / 2 ? "English" : "Pinyin") << '\n';
    }
}
