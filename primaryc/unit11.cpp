int unit11_main()
{
	int n,g,sh,b,q,y;
	cout << "请输入一个四位数:";
	cin>>n;
	g = n%10;
	sh = n/10%10;
	b = n/100%10;
	q = n/1000;
	y = g*1000+sh*100+b*10+q;
	cout<<y<<endl;
	return 0;
} 
